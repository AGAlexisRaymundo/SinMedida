document.getElementById('downloadBtn').addEventListener('click', function () {
    // La URL del archivo PDF alojado en la carpeta 'pdfs'
    var pdfUrl = 'PDFs/Artículo de divulgación 2020.pdf';

    // Crear un elemento de enlace (anchor)
    var link = document.createElement('a');
    link.href = pdfUrl;
    link.download = 'Artículo de divulgación 2020.pdf';

    // Agregar el enlace al DOM y simular un clic
    document.body.appendChild(link);
    link.click();

    // Eliminar el enlace del DOM
    document.body.removeChild(link);
});

